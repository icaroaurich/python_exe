## Por que uma variável com a mensagem e depois print da varável em vez de fazer um print diretamente com o texto? ##
#1- O seu print pode existir em diferentes linhas do código, e assim você não precisa reescrever todas as vezess
#2 - É interesante colocar todas as variáveis no inicio, para assim fácil modificação/manutenção, em vez de procurar em que linha está,
# pode parece bobagem agora, mas quando seu código tiver muitas linhas, vai ser mais fácil.